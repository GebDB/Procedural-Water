// settings
const unsigned int SCR_WIDTH = 1200;
const unsigned int SCR_HEIGHT = 720;

// camera
Camera camera(glm::vec3(0.0f, 6.0f, 10.0f));
float lastX = (float)SCR_WIDTH / 2.0;
float lastY = (float)SCR_HEIGHT / 2.0;
bool firstMouse = true;

// timing
float deltaTime = 0.0f;
float lastFrame = 0.0f;

int main()
{
    // glfw: initialize and configure
    // ------------------------------
    glfwInit();
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
    glfwWindowHint(GLFW_SAMPLES, 4);
    glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);

#ifdef __APPLE__
    glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);
#endif

    // glfw window creation
    // --------------------
    GLFWwindow* window = glfwCreateWindow(SCR_WIDTH, SCR_HEIGHT, "Water", NULL, NULL);
    if (window == NULL) {
        std::cout << "Failed to create GLFW window" << std::endl;
        glfwTerminate();
        return -1;
    }
    glfwMakeContextCurrent(window);
    glfwSetFramebufferSizeCallback(window, framebuffer_size_callback);
    glfwSetCursorPosCallback(window, mouse_callback);
    glfwSetScrollCallback(window, scroll_callback);
    glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);

    // Initialize GLAD
    if (!gladLoadGLLoader((GLADloadproc)glfwGetProcAddress)) {
        std::cout << "Failed to initialize GLAD" << std::endl;
        return -1;
    }

    // Configure OpenGL state
    glEnable(GL_DEPTH_TEST);
    glDepthFunc(GL_LEQUAL);

    // Build and compile shaders
    Shader shader = ResourceManager::LoadShader("water.vs", "water.fs", nullptr, "water");
    Shader equirectangularToCubemapShader = ResourceManager::LoadShader("cubemap.vs", "cubemap.fs", nullptr, "cubemap");
    Shader backgroundShader = ResourceManager::LoadShader("skybox.vs", "skybox.fs", nullptr, "skybox");
    //Shader skyboxShader = ResourceManager::LoadShader("skybox.vs", "skybox.fs", nullptr, "skybox");
    //Shader light = ResourceManager::LoadShader("light.vs", "light.fs", nullptr, "light");


    Model plane("assets/objects/plane.obj");


    // Shader configuration
    shader.Use();

    // fragment shader parameters
    glm::vec3 lightColor = glm::vec3(0.95f, 0.95f, 0.95f);
    glm::vec3 lightDirIn = glm::vec3(0.0f, 0.0f, 0.0f);
    float fresnelBias = 0.05f;
    float fresnelStrength = 0.5f;
    float fresnelShininess = 15.00f;
    float shininess = 10.0f;
    glm::vec3 ambient = glm::vec3(0.0f, 0.1f, 0.3f);
    glm::vec3 diffuseReflectance = glm::vec3(0.0f, 0.3f, 0.6f);
    glm::vec3 specularReflectance = glm::vec3(0.1f, 0.5f, 0.8f);
    glm::vec3 fresnelColor = glm::vec3(1.0f, 1.0f, 1.0f);
    float fogDensity = 0.05f;

    shader.SetVector3f("lightColor", lightColor);
    shader.SetVector3f("lightDirIn", lightDirIn);
    shader.SetFloat("shininess", shininess);
    shader.SetFloat("fresnelBias", fresnelBias);
    shader.SetFloat("fresnelStrength", fresnelStrength);
    shader.SetFloat("fresnelShininess", fresnelShininess);
    shader.SetVector3f("ambient", ambient);
    shader.SetVector3f("diffuseReflectance", diffuseReflectance);
    shader.SetVector3f("specularReflectance", specularReflectance);
    shader.SetVector3f("fresnelColor", fresnelColor);
    shader.SetFloat("fogDensity", fogDensity);

    // vertex shader parameters
    float amplitude = 7.5f;
    float frequency = 7.5f;
    float speed = 1.0f;
    shader.SetFloat("speed", speed);
    shader.SetFloat("amplitude", amplitude);
    shader.SetFloat("frequency", frequency);
    
    // skybox shader
    backgroundShader.Use();
    backgroundShader.SetInteger("environmentMap", 0);

    // pbr: setup framebuffer
    unsigned int captureFBO;
    unsigned int captureRBO;
    glGenFramebuffers(1, &captureFBO);
    glGenRenderbuffers(1, &captureRBO);

    glBindFramebuffer(GL_FRAMEBUFFER, captureFBO);
    glBindRenderbuffer(GL_RENDERBUFFER, captureRBO);
    glRenderbufferStorage(GL_RENDERBUFFER, GL_DEPTH_COMPONENT24, 512, 512);
    glFramebufferRenderbuffer(GL_FRAMEBUFFER, GL_DEPTH_ATTACHMENT, GL_RENDERBUFFER, captureRBO);
    
    // load the HDR environment map
    stbi_set_flip_vertically_on_load(true);
    int width, height, nrComponents;
    float* data = stbi_loadf("assets/skyboxhdr/CasualDay4K.hdr", &width, &height, &nrComponents, 0);
    unsigned int hdrTexture;

    if (data)
    {
        glGenTextures(1, &hdrTexture);
        glBindTexture(GL_TEXTURE_2D, hdrTexture);
        glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB16F, width, height, 0, GL_RGB, GL_FLOAT, data); // note how we specify the texture's data value to be float

        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

        stbi_image_free(data);
    }
    else
    {
        std::cout << "Failed to load HDR image." << std::endl;
    }

    // setup cubemap to render to and attach to framebuffer
    unsigned int envCubemap;
    glGenTextures(1, &envCubemap);
    glBindTexture(GL_TEXTURE_CUBE_MAP, envCubemap);
    for (unsigned int i = 0; i < 6; ++i)
    {
        glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB16F, 512, 512, 0, GL_RGB, GL_FLOAT, nullptr);
    }
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

    // pbr: set up projection and view matrices for capturing data onto the 6 cubemap face directions
    glm::mat4 captureProjection = glm::perspective(glm::radians(90.0f), 1.0f, 0.1f, 10.0f);
    glm::mat4 captureViews[] =
    {
        glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(1.0f,  0.0f,  0.0f), glm::vec3(0.0f, -1.0f,  0.0f)),
        glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(-1.0f,  0.0f,  0.0f), glm::vec3(0.0f, -1.0f,  0.0f)),
        glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f,  1.0f,  0.0f), glm::vec3(0.0f,  0.0f,  1.0f)),
        glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, -1.0f,  0.0f), glm::vec3(0.0f,  0.0f, -1.0f)),
        glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f,  0.0f,  1.0f), glm::vec3(0.0f, -1.0f,  0.0f)),
        glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f,  0.0f, -1.0f), glm::vec3(0.0f, -1.0f,  0.0f))
    };

    // pbr: convert HDR equirectangular environment map to cubemap equivalent
    equirectangularToCubemapShader.Use();
    equirectangularToCubemapShader.SetInteger("equirectangularMap", 0);
    equirectangularToCubemapShader.SetMatrix4("projection", captureProjection);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, hdrTexture);

    glViewport(0, 0, 512, 512); // don't forget to configure the viewport to the capture dimensions.
    glBindFramebuffer(GL_FRAMEBUFFER, captureFBO);
    for (unsigned int i = 0; i < 6; ++i)
    {
        equirectangularToCubemapShader.SetMatrix4("view", captureViews[i]);
        glFramebufferTexture2D(GL_FRAMEBUFFER, GL_COLOR_ATTACHMENT0, GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, envCubemap, 0);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        renderCube();
    }
    glBindFramebuffer(GL_FRAMEBUFFER, 0);

    // initialize static shader uniforms before rendering
     // --------------------------------------------------
    glm::mat4 projection = glm::perspective(glm::radians(camera.Zoom), (float)SCR_WIDTH / (float)SCR_HEIGHT, 0.1f, 100.0f);
    shader.Use();
    shader.SetMatrix4("projection", projection);
    backgroundShader.Use();
    backgroundShader.SetMatrix4("projection", projection);

    // then before rendering, configure the viewport to the original framebuffer's screen dimensions
    int scrWidth, scrHeight;
    glfwGetFramebufferSize(window, &scrWidth, &scrHeight);
    glViewport(0, 0, scrWidth, scrHeight);

    // initialize static shader uniforms before rendering
    // --------------------------------------------------
    shader.Use();
    shader.SetMatrix4("projection", projection);
    backgroundShader.Use();
    backgroundShader.SetMatrix4("projection", projection);

    glm::mat4 model = glm::mat4(1.0f);
    model = glm::scale(model, glm::vec3(114.0f, 1.0f, 114.0f));

    float time = glfwGetTime();
    // Render loop
    while (!glfwWindowShouldClose(window)) {
        // Per-frame timing
        float currentFrame = static_cast<float>(glfwGetTime());
        deltaTime = currentFrame - lastFrame;
        lastFrame = currentFrame;

        // Input
        processInput(window, lightDirIn);

        // Render
        glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        // Set uniforms
        glm::mat4 view = camera.GetViewMatrix();
        time = glfwGetTime();
        glm::vec3 cameraPos = glm::vec3(glm::inverse(view)[3]);


        shader.Use();
        shader.SetMatrix4("view", view);
        shader.SetMatrix4("projection", projection);
        shader.SetMatrix4("model", model);
        shader.SetFloat("time", time);
        shader.SetVector3f("viewPos", cameraPos);
        shader.SetVector3f("cameraPos", cameraPos);
        shader.SetVector3f("lightDirIn", lightDirIn);
        plane.Draw(shader);

        // render skybox (render as last to prevent overdraw)
        backgroundShader.Use();
        backgroundShader.SetMatrix4("view", view);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_CUBE_MAP, envCubemap);
        renderCube();


        // Swap buffers and poll events
        glfwSwapBuffers(window);
        glfwPollEvents();
    }

    // Cleanup
    glfwTerminate();
    return 0;
}

// process all input: query GLFW whether relevant keys are pressed/released this frame and react accordingly
// ---------------------------------------------------------------------------------------------------------
void processInput(GLFWwindow* window, glm::vec3& lightDirIn)
{
    if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)
        glfwSetWindowShouldClose(window, true);
    if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS)
        camera.ProcessKeyboard(FORWARD, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS)
        camera.ProcessKeyboard(BACKWARD, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS)
        camera.ProcessKeyboard(LEFT, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS)
        camera.ProcessKeyboard(RIGHT, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_1) == GLFW_PRESS) {
        lightDirIn.x += 0.01f;
        std::cout << "lightDirIn: ("
            << lightDirIn.x << ", "
            << lightDirIn.y << ", "
            << lightDirIn.z << ")" << std::endl;
    }
    if (glfwGetKey(window, GLFW_KEY_4) == GLFW_PRESS) {
        lightDirIn.x -= 0.01f;
        std::cout << "lightDirIn: ("
            << lightDirIn.x << ", "
            << lightDirIn.y << ", "
            << lightDirIn.z << ")" << std::endl;
    }
    if (glfwGetKey(window, GLFW_KEY_2) == GLFW_PRESS) {
        lightDirIn.y += 0.01f;
        std::cout << "lightDirIn: ("
            << lightDirIn.x << ", "
            << lightDirIn.y << ", "
            << lightDirIn.z << ")" << std::endl;
    }
    if (glfwGetKey(window, GLFW_KEY_5) == GLFW_PRESS) {
        lightDirIn.y -= 0.01f;
        std::cout << "lightDirIn: ("
            << lightDirIn.x << ", "
            << lightDirIn.y << ", "
            << lightDirIn.z << ")" << std::endl;
    }
    if (glfwGetKey(window, GLFW_KEY_3) == GLFW_PRESS) {
        lightDirIn.z += 0.01f;
        std::cout << "lightDirIn: ("
            << lightDirIn.x << ", "
            << lightDirIn.y << ", "
            << lightDirIn.z << ")" << std::endl;
    }
    if (glfwGetKey(window, GLFW_KEY_6) == GLFW_PRESS) {
        lightDirIn.z -= 0.01f;
        std::cout << "lightDirIn: ("
            << lightDirIn.x << ", "
            << lightDirIn.y << ", "
            << lightDirIn.z << ")" << std::endl;
    }
}

